// Release signing config is read from keystore.properties (gitignored) so the
// keystore + passwords never live in version control. Builds still work without
// it — the release type just stays unsigned in that case.
val keystorePropertiesFile = rootProject.file("keystore.properties")
val keystoreProperties = Properties().apply {
    if (keystorePropertiesFile.exists()) load(FileInputStream(keystorePropertiesFile))
}
val mangaKakalotBaseUrl = providers.gradleProperty("mangakakalotBaseUrl")
    .orElse("https://www.mangakakalot.gg")
    .get()
val mangaReaderBaseUrl = providers.gradleProperty("mangaReaderBaseUrl")
    .orElse("https://mangareader.to")
    .get()

android {
    namespace = "com.mangamojo.app"
    compileSdk {
        version = release(36) {
            minorApiLevel = 1
        }
    }

    defaultConfig {
        applicationId = "com.mangamojo.app"
        minSdk = 26
        targetSdk = 36
        versionCode = 2
        versionName = "1.1.0"

        testInstrumentationRunner = "androidx.test.runner.AndroidJUnitRunner"
        buildConfigField("String", "MANGAKAKALOT_BASE_URL", "\"${mangaKakalotBaseUrl.trimEnd('/')}\"")
        buildConfigField("String", "MANGAREADER_BASE_URL", "\"${mangaReaderBaseUrl.trimEnd('/')}\"")
    }

    signingConfigs {
        create("release") {
            if (keystorePropertiesFile.exists()) {
                storeFile = rootProject.file(keystoreProperties.getProperty("storeFile"))
                storePassword = keystoreProperties.getProperty("storePassword")
                keyAlias = keystoreProperties.getProperty("keyAlias")
                keyPassword = keystoreProperties.getProperty("keyPassword")
            }
        }
    }

    buildTypes {
        release {
            isMinifyEnabled = false
            proguardFiles(
                getDefaultProguardFile("proguard-android-optimize.txt"),
                "proguard-rules.pro"
            )
            if (keystorePropertiesFile.exists()) {
                signingConfig = signingConfigs.getByName("release")
            }
        }
    }
    compileOptions {
        sourceCompatibility = JavaVersion.VERSION_17
        targetCompatibility = JavaVersion.VERSION_17
    }
    buildFeatures {
        compose = true
        buildConfig = true
    }
}

